function makeCenterPoint()
{
    let marker = ce('div');
    marker.innerHTML = 'O';
    marker.id = 'marker';
    marker.style.position = 'absolute';
    marker.style.left = 320 + 'px';
    marker.style.top = 215 + 'px';
    marker.style.zIndex = 2;
    ba(marker);
}

